def choose_best_faq_answer(
    user_question: str,
    candidates: List[Dict[str, str]],
) -> Optional[Dict[str, str]]:
    """–í—ã–±–∏—Ä–∞–µ—Ç –ª—É—á—à–µ–≥–æ –∫–∞–Ω–¥–∏–¥–∞—Ç–∞ —Å—Ä–µ–¥–∏ FAQ —Å –ø–æ–º–æ—â—å—é GPT.

    candidates: —Å–ø–∏—Å–æ–∫ —Å–ª–æ–≤–∞—Ä–µ–π –≤–∏–¥–∞:
      { "question": str, "answer": str, "score": float }

    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –æ–¥–Ω–æ–≥–æ –∫–∞–Ω–¥–∏–¥–∞—Ç–∞ (dict) –∏–ª–∏ None, –µ—Å–ª–∏ GPT —Å—á–∏—Ç–∞–µ—Ç,
    —á—Ç–æ –Ω–∏ –æ–¥–∏–Ω –≤–∞—Ä–∏–∞–Ω—Ç –Ω–µ –ø–æ–¥—Ö–æ–¥–∏—Ç –¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ —Ö–æ—Ä–æ—à–æ.
    """
    if not candidates:
        return None

    # –°–æ–±–∏—Ä–∞–µ–º —Ç–µ–∫—Å—Ç –∫–∞–Ω–¥–∏–¥–∞—Ç–æ–≤ –¥–ª—è –ø—Ä–æ–º–ø—Ç–∞
    lines = []
    for i, cand in enumerate(candidates):
        lines.append(
            f"{i}. –í–æ–ø—Ä–æ—Å –∏–∑ –±–∞–∑—ã: {cand['question']}\n"
            f"   –û—Ç–≤–µ—Ç –∏–∑ –±–∞–∑—ã: {cand['answer']}\n"
            f"   –ë–∞–∑–æ–≤—ã–π score: {cand.get('score', 0):.3f}"
        )
    candidates_block = "\n\n".join(lines)

    system_prompt = (
        "–¢—ã –ø–æ–º–æ–≥–∞–µ—à—å –æ–ø–µ—Ä–∞—Ç–æ—Ä—É –ø–æ–¥–¥–µ—Ä–∂–∫–∏ —Å–µ—Ç–∏ –º–∞–≥–∞–∑–∏–Ω–æ–≤ –í–æ–±–ª–∞–±–∏—Ä. "
        "–¢–µ–±–µ –¥–∞—ë—Ç—Å—è –≤–æ–ø—Ä–æ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏ –Ω–µ—Å–∫–æ–ª—å–∫–æ –ø–æ—Ö–æ–∂–∏—Ö –≤–æ–ø—Ä–æ—Å–æ–≤ –∏–∑ –±–∞–∑—ã "
        "—Å –æ—Ç–≤–µ—Ç–∞–º–∏ –º–µ–Ω–µ–¥–∂–µ—Ä–æ–≤.\n\n"
        "–¢–≤–æ—è –∑–∞–¥–∞—á–∞:\n"
        "1) –ü–æ–Ω—è—Ç—å, –Ω–∞—Å–∫–æ–ª—å–∫–æ –∫–∞–∂–¥—ã–π –≤–∞—Ä–∏–∞–Ω—Ç –¥–µ–π—Å—Ç–≤–∏—Ç–µ–ª—å–Ω–æ –æ—Ç–≤–µ—á–∞–µ—Ç –Ω–∞ –≤–æ–ø—Ä–æ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.\n"
        "2) –ï—Å–ª–∏ –µ—Å—Ç—å –ø–æ–¥—Ö–æ–¥—è—â–∏–π –≤–∞—Ä–∏–∞–Ω—Ç, –≤—ã–±—Ä–∞—Ç—å –û–î–ò–ù –ª—É—á—à–∏–π.\n"
        "3) –ï—Å–ª–∏ –Ω–∏ –æ–¥–∏–Ω –≤–∞—Ä–∏–∞–Ω—Ç –Ω–µ –ø–æ–¥—Ö–æ–¥–∏—Ç –ø–æ —Å–º—ã—Å–ª—É, –Ω—É–∂–Ω–æ —è–≤–Ω–æ —É–∫–∞–∑–∞—Ç—å, "
        "—á—Ç–æ –ø–æ–¥—Ö–æ–¥—è—â–µ–≥–æ –≤–∞—Ä–∏–∞–Ω—Ç–∞ –Ω–µ—Ç.\n\n"
        "–û—á–µ–Ω—å –≤–∞–∂–Ω–æ: —Ç—ã –ù–ï –ø—Ä–∏–¥—É–º—ã–≤–∞–µ—à—å –Ω–æ–≤—ã–π –æ—Ç–≤–µ—Ç, –∞ —Ç–æ–ª—å–∫–æ –≤—ã–±–∏—Ä–∞–µ—à—å –ª—É—á—à–∏–π "
        "–∏–∑ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–Ω—ã—Ö –≤–∞—Ä–∏–∞–Ω—Ç–æ–≤."
    )

    user_prompt = (
        f"–í–æ–ø—Ä–æ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è:\n{user_question}\n\n"
        f"–ö–∞–Ω–¥–∏–¥–∞—Ç—ã –∏–∑ –±–∞–∑—ã:\n{candidates_block}\n\n"
        "–û—Ç–≤–µ—Ç—å —Å—Ç—Ä–æ–≥–æ –≤ —Ñ–æ—Ä–º–∞—Ç–µ:\n"
        "- –ï—Å–ª–∏ –æ–¥–∏–Ω –∏–∑ –≤–∞—Ä–∏–∞–Ω—Ç–æ–≤ –ø–æ–¥—Ö–æ–¥–∏—Ç, –Ω–∞–ø–∏—à–∏ —Ç–æ–ª—å–∫–æ –µ–≥–æ –Ω–æ–º–µ—Ä (0, 1, 2, ...), –±–µ–∑ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–µ–≤.\n"
        "- –ï—Å–ª–∏ –Ω–∏ –æ–¥–∏–Ω –≤–∞—Ä–∏–∞–Ω—Ç –Ω–µ –ø–æ–¥—Ö–æ–¥–∏—Ç, –Ω–∞–ø–∏—à–∏ —Å–ª–æ–≤–æ NONE.\n"
    )

    resp = client.chat.completions.create(
        model=CHAT_MODEL,
        messages=[
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt},
        ],
        temperature=0.0,
    )

    content = (resp.choices[0].message.content or "").strip()
    upper = content.upper()

    if upper.startswith("NONE"):
        return None

    # –ü—ã—Ç–∞–µ–º—Å—è —Ä–∞–∑–æ–±—Ä–∞—Ç—å –Ω–æ–º–µ—Ä –∫–∞–Ω–¥–∏–¥–∞—Ç–∞
    try:
        idx = int(content.split()[0])
    except ValueError:
        return None

    if 0 <= idx < len(candidates):
        return candidates[idx]

    return None



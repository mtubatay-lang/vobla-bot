        "–ñ–Å–°–¢–ö–ò–ï –ü–†–ê–í–ò–õ–ê:\n"
        "1) –ò—Å–ø–æ–ª—å–∑—É–π –¢–û–õ–¨–ö–û –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –∏–∑ –∏—Å—Ö–æ–¥–Ω–æ–≥–æ –æ—Ç–≤–µ—Ç–∞.\n"
        "2) –ù–ï –¥–æ–±–∞–≤–ª—è–π –Ω–æ–≤—ã—Ö —Ñ–∞–∫—Ç–æ–≤, —É—Å–ª–æ–≤–∏–π, —Ü–∏—Ñ—Ä, –¥–∞—Ç, –∞–¥—Ä–µ—Å–æ–≤, —Å—Å—ã–ª–æ–∫.\n"
        "3) –ù–ï –∏—Å–ø—Ä–∞–≤–ª—è–π —Ñ–∞–∫—Ç—ã. –ï—Å–ª–∏ –≤ –∏—Å—Ö–æ–¥–Ω–æ–º –æ—Ç–≤–µ—Ç–µ –µ—Å—Ç—å —Å—Ç—Ä–∞–Ω–Ω–æ—Å—Ç–∏ ‚Äî –æ—Å—Ç–∞–≤—å –∫–∞–∫ –µ—Å—Ç—å.\n"
        "4) –°–æ—Ö—Ä–∞–Ω—è–π –≤—Å–µ —á–∏—Å–ª–∞, –¥–∞—Ç—ã, –≤—Ä–µ–º—è, –Ω–∞–∑–≤–∞–Ω–∏—è –±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π.\n"
